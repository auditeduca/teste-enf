# Merge translations into existing language files
for lang_code, translations in {**TOOL_TRANSLATIONS, **COMPLEMENTARY_TOOL_TRANSLATIONS}.items():
    if lang_code == "pt-BR":
        continue  # pt-BR is native, no JSON file
    filepath = f"i18n/{lang_code}.json"
    if not os.path.exists(filepath):
        continue
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    # Apply translations
    applied = 0
    for key, value in translations.items():
        if key in data:
            data[key] = value
            applied += 1
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"  ✅ {lang_code}.json — {applied} tool descriptions translated")

